// Adding two numbers
let firstNumber = 10
let secondNumber = 5
let sum = firstNumber + secondNumber;
console.log("Sum:", sum);  // Output: Sum: 15
console.log(`Sum: ${sum}`);  // Same output as previous, the number will be a string instead of a number: Sum: 15

// Subtracting two numbers
let difference = 10 - 5;
console.log("Difference:", difference);  // Output: Difference: 5

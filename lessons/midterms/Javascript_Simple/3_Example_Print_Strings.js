// Printing a string to the console
console.log("Hello, World!");

// Printing a string with variables
let name = "Jericho";
console.log("Hello, " + name + "!");  // Output: Hello, Jericho!

// Printing a string with variables with mixed printing
let subject = "ELECT3";
console.log(`Hello, my name is ` + name + ` and I have a subject called ${subject}!`);
// Output: Hello, my name is Jericho and I have a subject called ELECT3!